#include <cstdio>
#include <cstdlib>
//cstdio is the C standard library for input and output operations, while cstdlib is the C standard library for general utilities, including memory allocation, process control, conversions, and others.

int main() {
    printf("hello, kitty\n");
    //\n is a newline character that moves the cursor to the next line after printing "hello, kitty".
    exit (0); // Return inndicates that the program ended successfully & exit (0) is a standard way to terminate a program in C/C++.
}
